public class BankDataPojo
{
	private String bankName;
	private String bankCode;
	private String baseStoreUId;

	/**
	 * @return the bankName
	 */
	public String getBankName()
	{
		return bankName;
	}

	/**
	 * @param bankName
	 *           the bankName to set
	 */
	public void setBankName(final String bankName)
	{
		this.bankName = bankName;
	}

	/**
	 * @return the bankCode
	 */
	public String getBankCode()
	{
		return bankCode;
	}

	/**
	 * @param bankCode
	 *           the bankCode to set
	 */
	public void setBankCode(final String bankCode)
	{
		this.bankCode = bankCode;
	}

	/**
	 * @return the baseStoreUId
	 */
	public String getBaseStoreUId()
	{
		return baseStoreUId;
	}

	/**
	 * @param baseStoreUId
	 *           the baseStoreUId to set
	 */
	public void setBaseStoreUId(final String baseStoreUId)
	{
		this.baseStoreUId = baseStoreUId;
	}



}
