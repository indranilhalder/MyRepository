package com.tisl.mpl.wsdto;

/**
 * @author TCS
 *
 */
public class TypeVariantWsDTO implements java.io.Serializable
{
	private String colour;
	private String size;


	/**
	 * @return the colour
	 */
	public String getColour()
	{
		return colour;
	}

	/**
	 * @param colour
	 *           the colour to set
	 */
	public void setColour(final String colour)
	{
		this.colour = colour;
	}

	/**
	 * @return the size
	 */
	public String getSize()
	{
		return size;
	}

	/**
	 * @param size
	 *           the size to set
	 */
	public void setSize(final String size)
	{
		this.size = size;
	}

}
